// i18n Class
class I18n {
    constructor() {
        this.currentLang = localStorage.getItem('lang') || 'ar';
        this.translations = translations;
    }
    
    init() {
        this.setLanguage(this.currentLang);
    }
    
    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('lang', lang);
        
        // Update HTML attributes
        document.documentElement.setAttribute('lang', lang);
        document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
        
        // Update body class for font switching
        document.body.classList.remove('lang-ar', 'lang-en');
        document.body.classList.add('lang-' + lang);
        
        // Update all translatable elements
        this.updateTranslations();
        
        // Language button text update removed as requested
        
        // Update active state of language options
        document.querySelectorAll('.lang-option').forEach(option => {
            option.classList.toggle('active', option.dataset.lang === lang);
        });
        
        // Reinitialize typed text for the new language
        if (window.typedInstance) {
            clearTimeout(window.typedInstance);
        }
        window.initTypedText();
        window.initAboutTypewriter();
        
        // Reinitialize AOS for new text
        if (typeof AOS !== 'undefined') {
            AOS.refresh();
        }
    }
    
    updateTranslations() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.getTranslation(key);
            if (translation) {
                // For select options, update the text content
                if (element.tagName === 'OPTION') {
                    element.textContent = translation;
                } else {
                    element.textContent = translation;
                }
            }
        });
        
        // Update placeholders
        const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
        placeholderElements.forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            const translation = this.getTranslation(key);
            if (translation) {
                element.setAttribute('placeholder', translation);
            }
        });
        
        // Update titles
        const titleElements = document.querySelectorAll('[data-i18n-title]');
        titleElements.forEach(element => {
            const key = element.getAttribute('data-i18n-title');
            const translation = this.getTranslation(key);
            if (translation) {
                element.setAttribute('title', translation);
            }
        });
    }
    
    getTranslation(key) {
        // First try direct key match (for keys like "hero.badge")
        const directMatch = this.translations[this.currentLang][key];
        if (directMatch !== undefined) {
            return directMatch;
        }
        
        // Fallback to nested key access
        const keys = key.split('.');
        let translation = this.translations[this.currentLang];
        
        for (const k of keys) {
            if (translation && translation[k] !== undefined) {
                translation = translation[k];
            } else {
                return null;
            }
        }
        
        return translation;
    }
    
    t(key) {
        return this.getTranslation(key);
    }
    
    getTypedWords() {
        return this.translations[this.currentLang]['hero.typed'] || [];
    }
}

// Create global instance
window.i18n = new I18n();
